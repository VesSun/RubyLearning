languages = {
  :de => 'German',
  :en => 'English',
  :es => 'Spanish',
}
dictionary = {
  :de => { :one => 'eins', :two => 'zwei', :three => 'drei' },
  :en => { :one => 'one', :two => 'two', :three => 'three' },
  :es => { :one => 'uno', :two => 'dos', :three => 'tres' }
}

languages.each do |key, value|
  dict = dictionary[key].map do |key, value|
    "#{value}"
  end
  puts "#{key} #{dict.join(" ")}"
end